                            <h3>Nuestro PVD (Punto Vive Digial)</h3>
                            <p> Nuestro Punto Vive Digital es un espacio que garantiza el acceso a las Tecnologías de la Información y las Comunicaciones mediante un modelo de servicios sostenible que permite integrar a la comunidad en escenarios de acceso, capacitación, entretenimiento,trámites de Gobierno en Línea y otras alternativas de servicios TIC en un mismo lugar, con el fin de promover y contribuir el aprovechamiento de las TIC´S con el desarrollo social y económico de la población y al mejoramiento de la calidad de vida de cada colombiano.</p> 
