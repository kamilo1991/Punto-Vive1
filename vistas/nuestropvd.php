                        <img src="../images/misionvision.jpg" class="img-responsive" alt="..">
                        <h4>Misión y Visión</h4>
                        <div class="border"></div>
                        <h4>Misión</h4>
<p>El Ministerio de Tecnologías de la Información y las Comunicaciones promueve el acceso, uso efectivo y apropiación masivos de las TIC, a través de políticas y programas, para mejorar la calidad de vida de cada colombiano y el crecimiento sostenible del desarrollo del país.</p>
<h4>Visión</h4>
<p>En el 2018, el Ministerio de las Tecnologías de la Información y las Comunicaciones estará preparado para enfrentar los retos de la economía digital y habrá logrado posicionar al país como referente en el desarrollo de aplicaciones sociales y contenidos digitales.

Contribuirá al desarrollo social y económico del país, al desarrollo integral de los ciudadanos y la mejora en su calidad de vida, mediante la investigación, promoción y apropiación del uso de nuevas tecnologías y la implementación de un modelo organizacional efectivo.</p>
