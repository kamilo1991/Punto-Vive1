                        <img src="../images/poli.png" class="img-responsive" alt="..">
                        <h4>Politica y Objetivos de Calidad </h4>
                        <div class="border"></div>
                        <h4>Política de calidad</h4>
<p>El Punto Vive Digital dentro de su  propósito es promover el acceso, uso y apropiación de las TIC para hacer más fácil la vida de los colombianos, se compromete a mejorar continuamente su gestión a través de:

Promover la innovación en las TIC que contribuyan al desarrollo de una sociedad próspera y equitativa.

Adoptar una cultura orientada al desarrollo y el servicio a través del reconocimiento continuo de las características, necesidades y expectativas de sus grupos de interés (Gobierno, Sector, Ciudadanía, Servidores).
</p>

<h4>Objetivos de calidad</h4>
<p>Promover el acceso uso y apropiación de las TIC en el Municipio.

Desarrollar un modelo que permita adelantar procesos de formación en las TIC´S con apoyo al sector empresarial y gubernamental.

Aumentar el nivel de satisfacción de los grupos de interés.

Fortalecer los niveles de clima y cultura organizacional.</p>
                    </div>
